<h4>Экскурсии в Гродно – на берега Немана</h4>
<p>
	 Гродно – западно-белорусский город, история которого насчитывает более 850 лет. Экскурсия в Гродно на берега Немана, где в 15-ти километрах от границы с Польшей и 30-ти километрах от границы с Литвой сегодня раскинулся современный город с 400-тысячным населением – это посещение, прежде всего, города древнеславянского!
</p>
<p>
	 Первое упоминание о Гродно датировано 1127-м годом – в Ипатьевской летописи Гродно (Городень) названо центром Гродненского княжества, которое стояло на западных рубежах славянского православного мира. В XIII веке город входит в состав Великого Княжества Литовского под владычеством Стефана Батория, а затем в состав Речи Посполитой.
</p>
<p>
	 В ходе экскурсии в Гродно вам предстоит познакомиться с «сердцем» Гродно – древним замком, который когда-то был возведен на руинах еще более древней крепости в месте впадения речки Городничанки в Неман. К XVI веку замок оброс ремесленным посадом, а рядом вырос Борисоглебский монастырь. Эпоха средневековых войн, которая привела к упадку города, завершилась в концу XVIII века появлением мануфактур Тизенгаузена, которые дали толчок бурному росту Гродно. Отголоски этих исторических событий вам предстоит увидеть в городской архитектуре, в которой смешались древнеславянские мотивы, немецкий классицизм, итальянское барокко, европейский Ренессанс, польская готика и русское Возрождение.
</p>
<p>
	 Экскурсии в Гродно – это глубокое погружение в городскую архитектурную культуру, отражающую историческое и культурное прошлое города. Центр архитектурного ансамбля – Замковая гора, которая считается национальным культурным заповедником.
</p>
