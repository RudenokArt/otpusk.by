<h4>Экскурсии в Полоцк – окунитесь в историю</h4>
<p>
	 Экскурсии в Полоцк – это посещение северной области Беларуси, где в 120 км от Витебска, на берегу реки Полоты (приток Западной Двины), раскинулся небольшой городок с населением всего 85 тысяч человек, многовековой истории которого могут позавидовать многие современные города-миллионники!
</p>
<p>
	 Экскурсии в Полоцк – не просто посещение самого древнего города Беларуси. Полоцк – один из самых старых городов мира всех восточнославянских племен, который еще дохристианской «Повести временных лет» (862 год) уже упоминался как большой город! Пограничному городку на речке Полоте довелось побывать в составе молодой Киевской Руси, торговать с викингами и греками, в Х веке превратиться в центр Полоцкого княжества с большим количеством христианских архитектурных святынь – здесь были построены Свято-Софийский собор (XI век), княжеский храм, Бельчицкий Борисоглебский и Спасо-Ефросиньевский монастыри (XII век). Город в XIV веке вошел в состав ВКЛ, получил права вольного города (Магдебургское право), стал центром Полоцкого воеводства Речи Посполитой, пережил несколько русско-польских войн. В 18 веке Полоцк вошел в состав Российской империи и с тех пор снова стал одним из православных центров.
</p>
<p>
	 В ходе экскурсий по Полоцку вы сможете посетить все перечисленные православные архитектурные святыни, узнаете о том, что в городе в разные времена жили и трудились Всеслав Чародей и княгиня Ольга, великий ювелир Лазарь Богша и зодчий Иоанн, здесь было напечатано Полоцкое Евангелие XII-XIV веков и создана самая древняя в Беларуси библиотека, здесь родился Франциск Скорина.
</p>
