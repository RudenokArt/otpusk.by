<h4>Экскурсии в Витебск – познание древней истории</h4>
<p>
	 Экскурсии в Витебск - один из самых старых городов Беларуси станет для вас настоящим откровением и знакомством с краем, который до сих пор вам был незнаком! Вы имеет возможность за несколько дней посетить древние города области, изучить экспозиции музеев и средневековую архитектуру края.
</p>
<p>
	 Экскурсия в Витебске – это посещение городского краеведческого музея, который работает с 1918 года и является наследником еще более старого музея, работавшего с 1868 года. Музей открыт в городской Ратуше, имеет художественный и литературный филиалы. Здесь же вас ждет посещение музея-усадьбы великого русского художника И. Е. Репина в Здравнево. Богатые экспозиции музея – национальное достояние Республики Беларусь.
</p>
<p>
	 Витебск считается родиной еще одного великого российского художника Марка Шагала, и в городе открыт его дом-музей и арт-центр его имени, где собрана большая коллекция произведений художника-авангардиста. Большой интерес также представляет художественный музей частного коллекционера Голькевича с экспозицией старинных наград, монет, часов и открыток. Экскурсия в Витебске – это посещение музея витебского трамвая и музея истории Башни Витебского замка, музей истории музыкального фестиваля «Славянский Базар».
</p>
<p>
	 Туристическая компания «Центркурорт» приглашает вас в экскурсии по Витебску, который стоит на древнем пути «из варяг в греки» и ведет свою родословную от княгини Ольги и Всеслава Чародея, Симеона Полоцкого и Франциска Скорины, помнит о своих великих земляках - Казимире Малевиче, Марке Шагале, Якубе Колосе и Петре Машерове, растет, развивается и вечно молодеет!
