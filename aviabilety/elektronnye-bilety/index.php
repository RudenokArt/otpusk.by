<h3>Как читать авиабилет</h3>
<p>
</p>
<p style="text-align: justify;">
	 1. FARE CALCULATION — Расчет тарифа. Здесь приводится расчет тарифа по данному авиабилету.
</p>
<p style="text-align: justify;">
	 2.FARE — Тариф. Тариф в валюте начала перевозки. В странах с неконвертируемой валютой (Россия) публикуют тарифы в долларах США. Иногда тариф бывает скрытым под аббревиатурой "IT" или "FORFAIT")&nbsp;
</p>
<p style="text-align: justify;">
	 3.TOTAL — Всего. Полная стоимость билета (тариф плюс аэропортовые сборы) в валюте пункта выписки билета. В России для разных авиакомпаний это могут быть как рубли РФ, так и доллары США. Также здесь может стоять IT.&nbsp;
</p>
<p style="text-align: justify;">
	 4.ORIGIN/DESTINATION — Пункт отправления/назначения. Трехбуквенное обозначение пункта вылета и через слеш, пункт прилета по всему маршруту. Бывают также обозначения SITI, SOTO и так далее.&nbsp;
</p>
<p style="text-align: justify;">
	 5.AIRLINE DATA — Номер бронирования. Буквенно-цифровой номер бронирования (PNR CODE).&nbsp;
</p>
<p style="text-align: justify;">
	 6.ENDORSEMENTS/RESTRICTIONS — Передаточная надпись/ограничения. В этой графе указываются различные ограничения на перевозку по данному билету, а также возможность «передачи» (endorsement) пассажира другому перевозчику. Например, если в графе указано "LH ONLY", то это означает, что данный билет действителен на рейсах "Lufthansa". Также здесь указываются разрешенные действия по даному тарифу. Например "REB FEE USD50" или "NON REF" означает: «штраф за изменение бронирования — 50 долларов США» или «возврат билета невозможен».&nbsp;
</p>
<p style="text-align: justify;">
	 7.NAME OF PASSENGER — Фамилия и Имя пассажира. Указывается только в латинской транскрипции (написании). После фамилии через слеш пишется первая буква имени или имя полностью и через пробел обозначение (MR - мистер, MRS/MSS - мисс или миссис). По нормам IATA данное поле допускает до 3-х ошибок.&nbsp;
</p>
<p style="text-align: justify;">
	 8.CONJUNCTION TICKETS — Соединенный билет. Если маршрут включает в себя больше перелетов, чем есть полетных купонов в бланке, такой билет выписывается на нескольких бланках и здесь ставятся перекрестные ссылки на другие номера билетов.&nbsp;
</p>
<p style="text-align: justify;">
	 9.STOPOVER — Индикатор «стоповер». X/O (нет/да). Если в данном городе происходит остановка более чем на 24 часа («стоповер»), то это поле пусто или там стоит "О". В случае, если пассажир не задерживается здесь более чем на сутки, то в этой графе стоит "Х". Применение стоповера отражается на общей стоимости билета за счет аэропортовых такс, так как при отсутствии «стоповер», некоторые таксы не платятся, что позволяет удешевить билет.&nbsp;
</p>
<p style="text-align: justify;">
	 10.ISSUED IN EXCHANGE FOR — Выдан в обмен на. Если билет обменен то в эту графу заносится номер первоначального билета.&nbsp;
</p>
<p style="text-align: justify;">
	 11.VALIDATOR PLACE — Поле валидатора. Штамп агентства, продавшего билет. Должен содержать название агентства, его краткий адрес, уникальный номер офиса, то есть номер ИАТА или код Аэрофлота. Буквенно-цифровая референция агента выписавшего билет. Дата выписки билета.&nbsp;
</p>
<p style="text-align: justify;">
	 12.FROM/TO — Маршрут перелета. Везде применяется английское написание названий городов и аэропортов. Каждый купон билета позволяет совершить перелет между двумя городами, которые на данном купоне выделены другим цветом (обычно темным). Города располагаются сверху вниз, то есть первый перелет будет между самым верхним пунктом и тем, который под ним. Если в городе несколько разных аэропортов, их трехбуквенные коды пишутся после города.&nbsp;
</p>
<p style="text-align: justify;">
	 13.CARRIER — Перевозчик. Двухбуквенный код авиакомпании перевозчика. Коды определяются ИАТА (Международная Ассоциация Авиационного Транспорта) например: SU — Аэрофлот, LH — Lufthansa, AF — Air France.&nbsp;
</p>
<p style="text-align: justify;">
	 14.FLIGHT — Номер рейса. CLASS — Класс бронирования. Латинская буква, обозначающая принадлежность поределенному классу. Обычно: F, P, A — первый класс. J, C, D, I, Z — бизнес класс. W, S, Y, B, H, K, L, M, N, Q, T, V, X — экономический класс.&nbsp;
</p>
<p style="text-align: justify;">
	 15.DATE — Дата вылета.&nbsp;
</p>
<p style="text-align: justify;">
	 16.TIME — Время вылета. Всегда местное время пункта вылета.&nbsp;
</p>
<p style="text-align: justify;">
	 17.STATUS — Статус бронирования. Обычно статус — "OK". Иногда возможна выписка билетов со статусом "RQ" (запрос места на регистрации). В билете для детей до 2-х лет, летящих без места — статус "NS".&nbsp;
</p>
<p style="text-align: justify;">
	 18.FARE BASIS — Вид тарифа. Буквенно-цифровое обозначение кода тарифа.&nbsp;
</p>
<p style="text-align: justify;">
	 19.NVB/NVA — Не действителен до/ не действителен после. Дата раннее которой и позднее которой соответственно нельзя лететь по данному полетному купону (если правила тарифа допускают изменения дат). Для большинства «жестких» льготных тарифов обе даты совпадают с датой вылета по данному купону. Если графы пустые, то это означает, что данный билет действителен как минимум год.
</p>
<p style="text-align: justify;">
	 20.Норма бесплатного провоза багажа. Здесь приводится расчет тарифа по данному авиабилету.&nbsp;
</p>
<p style="text-align: justify;">
	 21.TOUR CODE — Тур код .Код применяемый при выписке конфиденциальных и специальных тарифов.&nbsp;
</p>
<p style="text-align: justify;">
	 22.ADDITIONAL ENDORSEMENT/RESTRICTIONS — Дополнительные ограничения. Содержит информацию не поместившуюся в графу ENDORSEMENT/RESTRICTIONS.&nbsp;
</p>
<p style="text-align: justify;">
	 23.EQUIV/FARE — Эквивалент тарифа. Эквивалент тарифа в валюте пункта выписки (если валюта пункта выписки отличается от валюты пункта начала перевозки).&nbsp;
</p>
<p style="text-align: justify;">
	 24.TAX — Такса. Двухбуквенное обозначение и сумма аэропортового сбора. Если все таксы не помещаются в этой графе, то в последней графе указывается «общая» такса (сумма всех оставшихся).&nbsp;
</p>
<p style="text-align: justify;">
	 25.FORM OF PAYMENT — Форма оплаты. Форма оплаты билета. Бывают следующие варианты: CASH (наличные), INVOICE или INV (безналичная оплата), CC---номер кредитной карты--- (кредитная карта).&nbsp;
</p>
<p style="text-align: justify;">
	 26.Номер билета Первые три цифры — код авиакомпании. Например, 555 — Аэрофлот.<br>
</p>
