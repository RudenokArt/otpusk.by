<h3><b>Маршрут-квитанция</b></h3>
 <b>&nbsp;</b><br>
<p>
</p>
<p style="text-align: justify;">
	 Маршрут-квитанция - это лист формата А4, на котором содержится вся информация о маршруте, форме и деталях оплаты перевозки, аналогично бумажному билету, а именно: сведения о пассажире (для внутренних перевозок - фамилия, имя, отчество, наименование, серия и номер документа, удостоверяющего личность; для международных перевозок - фамилия, другие сведения, предусмотренные международными нормами);&nbsp;<br>
 <br>
	 • наименование и (или) код перевозчика;&nbsp;<br>
	 • номер рейса;&nbsp;<br>
	 • дата отправления рейса;&nbsp;<br>
	 • время отправления рейса;&nbsp;<br>
	 • наименование и (или) коды аэропортов/пунктов отправления и назначения для каждого рейса;&nbsp;<br>
	 • тариф;&nbsp;<br>
	 • эквивалент тарифа (если применяется);&nbsp;<br>
	 • итоговая стоимость перевозки;&nbsp;<br>
	 • форма оплаты;&nbsp;<br>
	 • сборы (если применяются);&nbsp;<br>
	 • наименование и (или) код класса бронирования;&nbsp;<br>
	 • код статуса бронирования;&nbsp;<br>
	 • дата оформления;&nbsp;<br>
	 • наименование агентства/перевозчика, оформившего билет;&nbsp;<br>
	 • норма бесплатного провоза багажа (по усмотрению);&nbsp;<br>
	 • уникальный номер электронного билета.&nbsp;<br>
 <br>
	 Маршрут-квитанция оформляется и выдается на руки пассажирам в обязательном порядке. Либо высылается ее электронный образ для последующей распечатки на e-mail при приобретении ЭБ через интернет сайт. Если Вам не выдали маршрут-квитанцию при покупке ЭБ, либо вы не получили e-mail или же у вас нет принтера для ее распечатки, не отчаивайтесь - всегда можно оформить дубликат. Сделать это можно либо в офисе авиакомпании либо непосредственно в аэропорту (но это в идеале, как реально обстоят дела с выдачей дубликата в каждом отдельном аэропорту неизвестно).&nbsp;<br>
 <br>
	 Для удобства пассажиров рекомендуется иметь маршрут-квитанцию при себе на протяжении всего пути следования. Это особенно важно в тех случаях, когда Вы вылетаете за границу. По прибытии в иностранное государство Вы должны иметь возможность пройти все процедуры службы безопасности и иммиграционной службы, которые могут потребовать подтверждения дальнейшего маршрута или обратного вылета из страны.&nbsp;<br>
 <br>
	 На регистрации Вы должны будете обязательно предъявить тот документ или форму идентификации, который был указан при бронировании и отображен в Вашей маршрут квитанции.&nbsp;<br>
 <br>
	 Маршрут квитанция - это не электронный билет. На ней лишь отображена основная информация из электронного билета необходимая пассажиру для комфорта. Как напоминание о маршруте следования, датах и портах вылета и тд.<b><br>
 </b>
</p>
