<h3 style="text-align: justify;">Статусы купонов электронного билета&nbsp;<br>
 </h3>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
 <b>A</b> - Airport Control (Аэропортовый контроль) Данный статус может иметь место в следующих случаях:&nbsp;<br>
 <b>C</b> - Checked-In (Прошел регистрацию) Пассажир зарегистрировал багаж и/ или получил посадочный талон. Данный статус может быть выставлен только при интерактивном взаимодействии СЭБ и DCS&nbsp;<br>
 <b>E</b> - Exchanged/ Reissued (Обменен/ переоформлен) Стоимость купона электронного билета была зачтена при оплате новой транзакции.&nbsp;<br>
 <b>F</b> - Flown| Used (Использован) Перевозка по полетному купону электронного билета выполнена.&nbsp;<br>
 <b>I</b> - Irregular operations (Неординарные операции) Фактический перевозчик, осуществляющий контроль над купоном, продлевает его сверх 48 часов, положенных для купонов с кодом состояния «A», «C» или «L».&nbsp;<br>
 <b>L</b> - Lifted| Boarded (Посадка произведена) Пассажир взошел на борт воздушного судна. Данный статус может быть выставлен только при интерактивном взаимодействии СЭБ и DCS&nbsp;<br>
 <b>O</b> - Open for use (Открыт для использования) Код состояния полетного купона может быть изменен на любой другой, кроме «T».&nbsp;<br>
 <b>P</b> - Printed (Распечатан) Полетные купоны электронного билета преобразованы из электронного файла в бумажный документ. После того, как купон распечатан, ему не может быть возвращен исходный электронный код состояния, и впоследствии он должен обрабатываться как бумажный полетный/ стоимостный купон.&nbsp;<br>
 <b>R</b> - Refunded (Произведен возврат денег) Неизрасходованная стоимость полетных купонов электронного билета возвращена или перечислена пассажиру.&nbsp;<br>
 <b>S</b> - Suspend (Приостановлен) Ответственный перевозчик временно ограничил использование полетного купона электронного билета.&nbsp;<br>
 <b>T</b> - Paper Ticket (Бумажный билет) Продажа была оформлена на бумажном носителе с номером, генерируемым системой. Этот код состояния купона позволяет перевозчикам хранить и/или отслеживать бумажные и электронные билеты в общей базе данных.&nbsp;<br>
 <b>U</b> - Unavailable (Недоступен для использования) Ответственный перевозчик обнаружил, что данный купон не доступен для использования. Такой купон может быть только обменен.&nbsp;<br>
 <b>V</b> - Void (Аннулирован) Полное аннулирование электронной записи о продаже. Применяется только в том случае, когда все купоны имеют код состояния «О». Аннулирование применимо только к первоначальной транзакции продажи или новому билету, оформленному в результате транзакции «Обмен/ Переоформление».&nbsp;<br>
 <b>X</b> - Print exchange (Заменен на бумажный документ) Электронный билет распечатан с новым номером билета без изменения первоначально оплаченного тарифа и соответствующих тарифных правил.&nbsp;<br>
 <b>Z</b> - Closed (Закрыт) Ответственный перевозчик запретил использование данного купона. 0–9 - Зарегистрирован для использования перевозчиком исключительно в онлайновом режиме.&nbsp;<br>
 <br>
	 Следующие коды состояния купона являются конечными (дальнейшие операции с купоном запрещены)&nbsp;<br>
 <br>
	 E – Обменен/ переоформлен&nbsp;<br>
	 X – Заменен на бумажный документ&nbsp;<br>
	 F – Использован V – Аннулирован&nbsp;<br>
	 P – Распечатан&nbsp;<br>
	 T – Бумажный билет&nbsp;<br>
	 R – Произведен возврат денег&nbsp;<br>
	 Z – Закрыт
</p>
