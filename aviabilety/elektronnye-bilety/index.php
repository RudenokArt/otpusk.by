<h3> Изменение и возврат</h3>
<p>
</p>
<p style="text-align: justify;">
 <br>
</p>
<p style="text-align: justify;">
	 В каком случае можно получить к возврату полную стоимость авиабилета? Вынужденный и добровольный возврат.&nbsp;<br>
 <br>
	 Вынужденным возвратом (возврат полной стоимости даже "невозвращаемого" билета) считается возврат в случае:&nbsp;<br>
 <br>
	 • Отмены, переноса, а также задержки рейса перевозчиком.&nbsp;<br>
	 • Отмены посадки в пункте, указанном в билете пассажира.&nbsp;<br>
	 • Не обеспечение перевозчиком стыковки рейсов.&nbsp;<br>
	 • Замена класса обслуживания или типа воздушного судна.&nbsp;<br>
	 • Смерти пассажира или членов семьи.&nbsp;<br>
 <br>
 <br>
	 Возврат вследствие других причин считается добровольным&nbsp;и осуществляется по правилам тарифа, по которому данный билет выписан. В этом случае на полный возврат можно рассчитывать только по тарифам первого, бизнес, полного годового и экскурсионного тарифа экономического класса. Следует иметь в виду, что по большинству льготных тарифов добровольный возврат не производится вообще, поэтому, о правилах возврата Вашего билета стоит поинтересоваться еще до покупки последнего.&nbsp;<br>
 <br>
 <b>Где можно произвести возврат билета?&nbsp;</b><br>
	 Продавая перевозку, Агент авиакомпании получает определенное комиссионное вознаграждение, которое при возврате необходимо вернуть пассажиру, из чего следует, что возврат авиабилета может произвести ТОЛЬКО Агентство, выписавшее билет.&nbsp;<br>
 <br>
 <b>В течение, какого времени можно осуществить возврат билета?&nbsp;</b><br>
	 Для большинства льготных тарифов существуют временные ограничения на возврат авиабилета. Конечно, лучше эти подробности узнать до покупки билета. Наиболее распространенные условия: более чем за 7 дней до вылета, более чем за 24 часа до вылета, до вылета. Обычно, после вылета (в случае неявки на рейс) по льготным тарифам возврат не производится.&nbsp;<br>
	 Для нормальных тарифов (первый, бизнес и полный годовой тариф экономического класса), обычно возврат можно осуществить в течение одного года от даты вылета.&nbsp;<br>
 <br>
 <b>Как рассчитывается возврат по частично использованному билету?&nbsp;</b><br>
	 Большинство льготных тарифов возврат по частично использованным билетам не допускают. Для "нормальных" тарифов возврат производится за вычетом РЕАЛЬНО ИСПОЛЬЗОВАННОЙ перевозки. Рассмотрим пример. Пассажир купил билет по маршруту Москва - Женева - Москва, выписанный по экскурсионному тарифу ($590). Пролетев участок Москва - Женева, пассажир вернулся каким-то другим маршрутом и предъявил неиспользованный участок (Женева - Москва) к возврату. Вычитая из тарифа стоимость перелета Москва - Женева ($475 - полный годовой тариф), получаем сумму к возврату $590-$475=$115, а не половину от $590, как казалось (хотелось) бы. Еще более показательный пример. Билет бизнес класса (тариф USD 2016) выписан по маршруту Лондон - Хельсинки - Мюнхен - Рим - Женева - Цюрих - Москва. Пассажир пролетел по билету всего лишь участки Лондон - Хельсинки и Женева - Цюрих - Москва, но стоимость по такому маршруту те же $2016 и, следовательно, сумма к возврату за неиспользованные участки Хельсинки - Мюнхен - Рим - Женева равна $0.&nbsp;<br>
 <br>
 <b>Переписка авиабилета&nbsp;</b><br>
	 Некоторые тарифы не допускают возврата по билету, но сумма тарифа может быть зачтена в стоимость другого билета, обычно по более высокому (более дорогому) тарифу (часто даже по другому маршруту). В этом случае первоначальный билет представляет собой эквивалент некой суммы (эта сумма всегда не возвращаемая), которым можно расплатиться за новый билет при соблюдении некоторых условий. О возможности такой операции следует поинтересоваться в Вашем агентстве.<br>
