<h2>Электронное билетооформление</h2>
<p>
</p>
<p style="text-align: justify;">
	 Электронное билетооформление - это метод документирования продажи перевозки при которой детали билета хранятся в компьютерной системе авиакомпании - базе данных электронных билетов. Доступ и обновление информации билета осуществляется различными способами, делая возможным те же действия, что и с бумажным билетом. После осуществления перевозки информация передается в бухгалтерию по доходам. При покупке ЭБ пассажирам предоставляются чеки об оплате и извещения, также предоставляется возможность самостоятельной распечатки маршрут-квитанции.&nbsp;<br>
 <br>
 <b> </b>
</p>
<h3><b>Преимущества ЭБ перед бумажным</b></h3>
 <b>&nbsp;</b><br>
<p>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 • наименование и (или) код перевозчика;&nbsp;<br>
	 • номер рейса;&nbsp;<br>
	 • дата отправления рейса;&nbsp;<br>
	 • время отправления рейса;&nbsp;<br>
	 • наименование и (или) коды аэропортов/пунктов отправления и назначения для каждого рейса;&nbsp;<br>
	 • тариф;&nbsp;<br>
	 • эквивалент тарифа (если применяется);&nbsp;<br>
	 • итоговая стоимость перевозки;&nbsp;<br>
	 • форма оплаты;&nbsp;<br>
	 • сборы (если применяются);&nbsp;<br>
	 • наименование и (или) код класса бронирования;&nbsp;<br>
	 • код статуса бронирования;&nbsp;<br>
	 • дата оформления;&nbsp;<br>
	 • наименование агентства/перевозчика, оформившего билет;&nbsp;<br>
	 • норма бесплатного провоза багажа (по усмотрению);&nbsp;<br>
	 • уникальный номер электронного билета.&nbsp;<br>
