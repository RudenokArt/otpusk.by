<p style="text-align: justify;">
 <b>Процедура бронирования.</b>
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Процедура бронирования состоит из трех этапов. Первый этап заключается в подборе оптимального для вас варианта перелета, второй этап представляет собой непосредственное бронирование мест. На третьем этапе вы выбираете форму оплаты и офис оформления билета.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 1.&nbsp;На первом этапе Вам необходимо заполнить предлагаемую форму, где вы указываете направление, даты вылета. Есть возможность выбрать перелет в одну сторону, туда-обратно. После заполнения формы нажмите кнопку&nbsp;ПОИСК.&nbsp;Подождите в течение 10-20 сек., т.к. системе необходимо время для обработки Вашего запроса.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 2.&nbsp;После того, как система выдаст Вам возможные варианты по Вашему запросу, выберите оптимальный для Вас вариант и нажмите кнопку&nbsp;ВЫБРАТЬ.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 3.&nbsp;На втором этапе необходимо проверить детали выбранного Вами варианта перелета (направление, даты, время вылета/прилета), стоимость. Обязательно ознакомьтесь с условиями применения выбранного Вами тарифа.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 4.&nbsp;Заполните форму, где указывается информация о пассажирах. Обязательные к заполнению графы отмечены звездочкой. Фамилия и имя пассажира вносятся латинскими буквами.(Должно быть полное соответствие написания на латинице, как в паспорте).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 5.&nbsp;Теперь перейдите на последний этап по кнопке&nbsp;СЛЕДУЮЩИЙ ШАГ. Этот этап обязателен для завершения резервирования.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 6.&nbsp;Выберите адрес офиса, наиболее удобного для выкупа авиабилета.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 7.&nbsp;Поставьте&nbsp;ГАЛОЧКУ&nbsp;напротив строки&nbsp;СОГЛАСЕН С ПРАВИЛАМИ&nbsp;и нажмите кнопку&nbsp;ЗАБРОНИРОВАТЬ.&nbsp;Сохраните информацию о номере Вашего бронирования.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 8.&nbsp;Вы самостоятельно забронировали авиабилет(ы). Дождитесь звонка нашего агента по бронированию с тем, чтобы уточнить Ваше решение по сделанному бронированию, а также детали оплаты.
