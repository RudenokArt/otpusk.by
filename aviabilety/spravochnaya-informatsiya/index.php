<h3>
Таможенные правила Республики Беларусь<br>
 </h3>
<h2>ПОРЯДОК ПЕРЕМЕЩЕНИЯ ФИЗИЧЕСКИМИ ЛИЦАМИ ТОВАРОВ ДЛЯ ЛИЧНОГО ПОЛЬЗОВАНИЯ ЧЕРЕЗ ТАМОЖЕННУЮ ГРАНИЦУ ТАМОЖЕННОГО СОЮЗА</h2>
<p>
	 В отношении норм беспошлинного ввоза за основу приняты самые льготные условия законодательства трех сторон: Республики Беларусь, Республики Казахстан и Российской Федерации.
</p>
<p>
	 Так, норма беспошлинного ввоза личного имущества, ввозимого в сопровождаемом и несопровождаемом багаже, составляет в стоимостном выражении 1 500 евро (для авиатранспорта – 10 000 евро), а в весовом – с 50 кг. Однако, не следует думать, что теперь можно ввезти 50 кг однородных товаров.
</p>
<p>
	 В указанные нормы беспошлинного ввоза в соответствии с Соглашением включаются алкогольные напитки и пиво в количестве до 3 литров, а также 200 сигарет или 50 сигар (сигарилл), или 250 граммов табака либо указанные изделия в ассортименте общим весом не более 250 граммов. Причем ввоз алкогольных напитков, пива, табака и табачных изделий может осуществляться только физическими лицами, достигшими 18-летнего возраста. Если же количество ввозимых алкогольных напитков и пива превысит 3 литра, необходимо будет уплатить таможенные платежи по единой ставке 10 евро за 1 литр в части превышения, ввоз более 5 литров вообще запрещен. Аналогичный запрет распространяется и на табак и табачные изделия, ввозимые в количестве, превышающем вышеуказанное.
</p>
<p>
	 Исключены количественные ограничения на ввоз ювелирных изделий и продуктов питания. Теперь указанные категории товаров включаются в общую норму (50 кг и 1500 евро). Данные нормы касаются товаров для личных целей.
</p>
<p>
	 В случае перемещения товаров любым способом в количестве, превышающем нормы беспошлинного ввоза, применяется ставка таможенных пошлин, налогов, равная 30% от стоимости, но не менее 4 евро за 1 кг.
</p>
<h3>
Советы при покупке авиабилетов<br>
 </h3>
<h4>Как купить дешевые авиабилеты: руководство пользователя</h4>
<p>
	 В наше время самостоятельные путешественники и даже бизнес-туристы все чаще стремятся отыскать недорогие варианты перелета по своему маршруту.
</p>
<p>
	 Но как купить дешевые авиабилеты? Где их продают? Все в ваших руках. Если планировать путешествие заранее и не в «высокий» сезон, то появляется целый ряд способов сэкономить на перелетах. Предлагаем вам материал о том, как быстро отыскать подходящую авиакомпанию и не переплачивать за билет на самолет.
</p>
<p>
	 1) Специальные предложения авиакомпаний
</p>
<p>
	 Заблаговременно до вылета необходимо внимательно следить за распродажами и специальными предложениями авиакомпаний. Как правило, распродажи авиабилетов случаются два раза в год: ранней весной и ранней осенью. Отдельные компании проводят акции также и ближе к крупным праздникам, Новому году или Рождеству. Чаще всего такие распродажи длятся очень недолго, но пассажирам в этот период предлагаются реально низкие тарифы. Промежуток времени, в течение которого можно совершить перелет скорее всего будет ограничен (как правило, это несколько месяцев в «низкий» сезон).
</p>
<p>
	 Количество мест по дешевым тарифам на каждом рейсе обычно небольшое и предлагаются они, чаще всего, не на все рейсы и даты вылета. Будьте готовы к тому, что сдать или переоформить акционный билет на другие даты невозможно (в некоторых случаях переоформление возможно за дополнительную плату); возможны ограничения объема провозимого багажа; тарифом может быть ограничен круг лиц, которым предоставляется скидка (например, пенсионеры, студенты и т.д.).
</p>
<p>
	 Компании, выпускающие на рынок спецпредложения, негативно относятся к «отказникам». Поэтому, если существует вероятность того, что вам придется отложить обратный рейс, не приобретайте билеты с фиксированной датой. В таком случае лучше приобрести документ с открытой датой или потратиться «полноценный» билет.
</p>
<p>
</p>
<p>
	 2. Поиск при помощи билетных агрегаторов
</p>
<p>
	 Для сравнения стоимости авиабилетов пригодятся сайты «агрегаторы». Такие сервисы собирают в себя информацию о предложениях различных авиакомпаний. Уже за 1-2 месяца до желаемой даты вылета можно начинать мониторинг ресурсов, например, на&nbsp;&nbsp;&nbsp;otpusk&nbsp;.by.
</p>
<p>
	 После выдачи результатов сайт-агрегатор перенаправит вас на тот ресурс, на котором он нашел выбранный вами тариф. Забронировать билет можно будет непосредственно на данном ресурсе. Будьте готовы к тому, что сайт-первоисточник может не иметь русскоязычной версии.
</p>
<p>
	 &nbsp;
</p>
<p>
	 3. Бюджетные авиакомпании (дискаунтеры или лоукостеры)
</p>
<p>
	 Авиакомпании-дискаунтеры явление совсем не новое, однако, в аэропорты Беларуси они практически не залетают. Точнее, в белорусском небе сейчас можно встретить лишь AirBaltic, компанию, которую принято считать бюджетной. Однако это вполне «классический» перевозчик, имеющий ряд привлекательных тарифов. Поэтому дешевые авиарейсы из Минска на данный момент большая редкость. В государства СНГ лучше подыскать рейс из Москвы, Санкт-Петербурга или Киева.
</p>
<p>
	 В России же неплохо зарекомендовал себя SkyExpress и популярны рейсы дискаунтеров AirBerlin и GermanWings, которые предлагают дешевые авиабилеты на внутренние перелеты, а также на маршруты в ближайшие страны Евросоюза. Немецкий дискаунтер GermanWings устраивает акции по распродаже летних билетов в конце зимы, а купить билеты со скидкой можно лишь в течение суток. За это время авиакомпания продает порядка 100 000 дешевых авиабилетов. Отправиться в путешествие без значительной нагрузки для кошелька получится из Каунаса, Риги, Вильнюса, Киева или Варшавы.
</p>
<p>
	 Билеты бюджетных авиакомпаний иногда настолько дешевые, что стоят в полтора-два раза ниже, чем у конкурентов. Это достигается за счет того, что бюджетные авиаперевозчики стараются экономить на всем. Часто их лайнеры подымают из неудобных аэропортов, а полеты осуществляются в неудобное время. Будьте готовы к прибытию в провинциальный аэропорт.
</p>
<p>
	 Переживать из-за состояния самолетного парка лоукостеров не стоит, потому что он, как правило, как раз новый. Это позволяет существенно экономить средства за счет более редкого обслуживания машин, а их современные двигатели потребляют меньше топлива. В общем, если вы готовы ради экономии пожертвовать комфортом, то бюджетные авиакомпании – ваш выбор.
</p>
<p>
	 Отправившись в полет с дискаунтером вам придется оплатить все сопряженные услуги: регистрацию, питание, доставку багажа. Пассажирам вполне могут быть предложены билеты без указания посадочного места. Вам придется занимать любое свободное сиденье, как в наземной электричке. Как правило, пассажиров на борту не кормят. Нормы провоза багажа и ручной клади значительно снижены.
</p>
<p>
	 Имейте в виду, что рейсы лоукостеров задерживают и отменяют чаще, чем у других компаний. Это особенно актуально в тех случаях, когда вы добираетесь до места назначения с пересадкой.
</p>
