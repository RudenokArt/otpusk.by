?><h2>Как оформить санаторно-курортную карту</h2>
<p style="text-align: justify;">
	 Лечение граждан в санаторно-курортных и оздоровительных учреждениях Республики Беларусь осуществляется на основании данных санаторно-курортной карты или формы 1 медучреждения «Выписка из медицинских документов» (действительно для граждан Республики Беларусь).
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В санаторно-курортную карту все диагнозы записываются согласно Международной классификации болезней в виде шифра, который является универсальным для всех стран. Поэтому, где бы ни была оформлена санаторно-курортная карта ее правильно поймет, любой врач.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 В исключительных случаях, в некоторых санаториях, имеющих соответствующею лечебно-диагностическую базу, санаторно-курортную карту можно оформить на месте, в самом санатории. Для этого Вам необходимы:
</p>
<p style="text-align: justify;">
</p>
<ul style="text-align: justify;">
	<li>амбулаторная карта (выписка из амбулаторной карты);</li>
	<li>флюорография;</li>
	<li>осмотр гинеколога (для женщин).</li>
</ul>
<p>
</p>
<p style="text-align: justify;">
	 Оформление санаторно-курортной карты в санатории занимает от 2 до 3 дней, в которые лечение не может быть назначено, так как не будет результатов обследований и показаний для лечения. А в случае, если отдыхающие приезжают накануне выходных (пятница после обеда, суббота, воскресенье) без санаторно-курортной карты, то прием врача и назначения на обследования они получат только в понедельник. В результате теряется 4 дня лечения.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Существуют показания и противопоказания к лечению в санаториях. Очень часто пациенты, не до конца зная свои диагнозы, выбирают лечебный профиль санатория, исходя из жалоб и болевых симптомов. Однако эти диагнозы после обследования в самом санатории могут не подтвердится. В результате получается, что человек приехал, заплатил за путевку, но лечение получить не может, так как оно противопоказано.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Оформление формы 1 мед/у-10 в Республике Беларусь регламентируется Постановлением Министерства здравоохранения Республики Беларусь 09.07.2010 № 92.
</p>
