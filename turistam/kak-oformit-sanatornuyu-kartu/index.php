<h2 style="text-align: justify;">Инструкция о порядке заполнения формы 1 мед/у-10 «Выписка из медицинских документов» (выдержка):</h2>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Медицинская справка о состоянии здоровья и выписка из медицинских документов являются унифицированными формами медицинских документов, содержание и объем информации в которых зависят от цели их предоставления.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 Медицинская справка о состоянии здоровья и выписка из медицинских документов заполняются согласно указанным в установленных формах графам и содержат необходимую пациенту информацию о результатах его медицинского наблюдения за определенный интервал времени. Независимо от цели предоставления в документы, указанные в части первой настоящего пункта, в обязательном порядке вносятся следующие сведения: фамилия, собственное имя, отчество гражданина, дата рождения, место жительства, цель выдачи медицинской справки о состоянии здоровья и (или) выписки из медицинских документов, дата выдачи медицинской справки о состоянии здоровья и (или) выписки из медицинских документов, срок действия медицинской справки о состоянии здоровья.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 При выдаче медицинской справки о состоянии здоровья, определяющей нуждаемость в санаторно-курортном лечении, в графе «Перенесенные заболевания (иные анамнестические сведения)» кроме заболеваний указываются сведения о контакте с инфекционными больными, результаты осмотра на заразные кожные заболевания и педикулез (для детей). В графу «Дополнительные медицинские сведения (результаты медицинских осмотров, обследований, сведения о прививках и прочее)» вносится информация о рентгенофлюорографическом обследовании. В графу «Заключение» вносятся сведения о диагнозе и заключение о нуждаемости в санаторно-курортном лечении. В графе «Рекомендации» указывается профиль рекомендуемого санаторно-курортного лечения. В графе «Срок действия справки» указывается шесть месяцев.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 При выдаче выписки из медицинских документов для санаторно-курортного лечения в графе «Выписка дана для предоставления» указывается наименование санатория. В графу «Перенесенные заболевания (иные анамнестические сведения)» вносятся сведения о перенесенных заболеваниях, о наследственности, аллергоанамнез с указанием переносимости лекарственных средств. В графе «Дополнительные медицинские сведения (результаты медицинских осмотров, обследований, сведения о прививках и прочее)» указываются профилактические прививки, сведения о приеме гормональных препаратов, рентгенофлюорографическое обследование, данные лабораторных (общий анализ крови, общий анализ мочи и прочее) и инструментальных обследований, жалобы, анамнез заболевания, результаты объективного осмотра, результаты осмотра врачей-специалистов, результаты осмотра на заразные кожные заболевания и педикулез. В графе «Проведенное лечение» указываются сведения о предшествующем, в том числе санаторно-курортном, лечении.
</p>
<p style="text-align: justify;">
</p>
<p style="text-align: justify;">
	 При заполнении медицинской справки о состоянии здоровья и (или) выписки из медицинских документов дополнительные медицинские сведения и иные обязательные сведения, занимающие большой объем, могут располагаться на обороте медицинской справки о состоянии здоровья и (или) выписки из медицинских документов с заверением подписью ответственного за выдачу данного документа должностного лица и печатью учреждения здравоохранения.
